    private void cus_reg_table() {
        try {
            ResultSet item_list = Database.getData("SELECT\n"
                    + "cus_reg.cus_id,\n"
                    + "cus_reg.cus_code,\n"
                    + "cus_reg.cus_name,\n"
                    + "cus_reg.cus_add,\n"
                    + "cus_reg.cus_tp,\n"
                    + "cus_reg.cus_con_person,\n"
                    + "cus_reg.cus_con_tp,\n"
                    + "cus_reg.credit_limit,\n"
                    + "cus_reg.nic,\n"
                    + "cus_reg.`status`\n"
                    + "FROM\n"
                    + "cus_reg\n"
                    + "ORDER BY\n"
                    + "cus_reg.cus_id DESC");

            DefaultTableModel tMOdel = (DefaultTableModel) cus_reg_tbl.getModel();
            tMOdel.setRowCount(0);
            while (item_list.next()) {
                ArrayList arrList = new ArrayList();
                arrList.add(item_list.getString("cus_id"));
                arrList.add(item_list.getString("cus_code"));
                arrList.add(item_list.getString("cus_name"));
                arrList.add(item_list.getString("cus_add"));
                arrList.add(item_list.getString("cus_tp"));
                arrList.add(item_list.getString("cus_con_person"));
                arrList.add(item_list.getString("cus_con_tp"));
                arrList.add(item_list.getString("credit_limit"));
                tMOdel.addRow(arrList.toArray());
            }

        } catch (Exception ex) {
            System.out.println(ex);
        }
    }


