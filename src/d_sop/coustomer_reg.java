    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
            String c_code = cous_code.getText();
            String c_name = jTextField3.getText();
            String c_add = jTextField1.getText();
            String c_tp = jTextField2.getText();
            String c_con_p = jTextField4.getText();
            String c_con_tp = jTextField6.getText();
            String c_cradit = jTextField8.getText();

            if (c_code.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Customer code not to be empty!", "Erro", JOptionPane.INFORMATION_MESSAGE);
            }
            if (c_name.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Customer Name not to be empty!", "Erro", JOptionPane.INFORMATION_MESSAGE);
            }
            if (c_add.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Customer Address not to be empty!", "Erro", JOptionPane.INFORMATION_MESSAGE);
            }
            if (c_tp.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Customer TP not to be empty!", "Erro", JOptionPane.INFORMATION_MESSAGE);
            }
            if (c_con_p.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Contact Person not to be empty!", "Erro", JOptionPane.INFORMATION_MESSAGE);
            }
            if (c_con_tp.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Contact Person TP not to be empty!", "Erro", JOptionPane.INFORMATION_MESSAGE);
            }
            if (c_cradit.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Cradit Limit not to be empty!", "Erro", JOptionPane.INFORMATION_MESSAGE);
            }

            boolean update = Database.putData("UPDATE `cus_reg` SET  `cus_code`='"+ c_code +"', `cus_name`='"+ c_name +"', `cus_add`='"+ c_add +"', `cus_tp`='"+ c_tp +"', `cus_con_person`='"+ c_con_p +"', `cus_con_tp`='"+ c_con_tp +"', `credit_limit`='"+ c_cradit +"'  WHERE (`cus_id`='"+edit_id+"');");
            if (update) {
                JOptionPane.showMessageDialog(null, "successfully Updated !", "Title", JOptionPane.INFORMATION_MESSAGE);
                cus_reg_table();
            } else {
                JOptionPane.showMessageDialog(null, "Update  Errro !", "Title", JOptionPane.INFORMATION_MESSAGE);
                
            }

        

    }//GEN-LAST:event_jButton3ActionPerformed

